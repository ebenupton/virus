#!/usr/bin/env python3
"""
Validate raster.s Bresenham line drawer against a Python reference.

Assembles a test harness (ca65+ld65), runs it through w65c02s_runner,
extracts pixels from the stripe-based framebuffer, and compares with
a reference Bresenham implementation.
"""

import os
import random
import struct
import subprocess
import sys
import tempfile

TOOLS_DIR  = os.path.join(os.path.dirname(os.path.abspath(__file__)), "tools")
RUNNER     = os.path.join(TOOLS_DIR, "w65c02s_runner")
LINKER_CFG = os.path.join(TOOLS_DIR, "linker_65c02.cfg")
RASTER_S   = os.path.join(os.path.dirname(os.path.abspath(__file__)), "raster.s")

SCREEN_BASE_HI = 0x20          # screen at $2000
SCREEN_BASE    = SCREEN_BASE_HI << 8


# ── Reference Bresenham ──────────────────────────────────────────────

def reference_shallow(x0, y0, x1, y1):
    """
    Shallow path reference (dx >= |dy|).
    Requires x1 >= x0, dx >= dy.
    """
    dx = x1 - x0
    dy = abs(y0 - y1)
    going_down = y1 > y0
    pixels = set()
    d = dx - dy
    if going_down:
        # Backward traversal: start at (x1, y1), walk left, y upward
        y = y1
        for i in range(dx + 1):
            pixels.add((x1 - i, y))
            d -= dy
            if d < 0:
                d += dx
                y -= 1
    else:
        # Forward traversal: start at (x0, y0), walk right, y upward
        y = y0
        for x in range(x0, x1 + 1):
            pixels.add((x, y))
            d -= dy
            if d < 0:
                d += dx
                y -= 1
    return pixels


def reference_steep(x0, y0, x1, y1):
    """
    Steep path reference (|dy| > dx).
    Requires x1 >= x0, dy > dx.
    """
    dx = x1 - x0
    dy = abs(y0 - y1)
    going_down = y1 > y0
    pixels = set()
    d = dy - dx
    if going_down:
        # Reverse: start at (x1, y1), walk up, x moves left
        x = x1
        for i in range(dy + 1):
            pixels.add((x, y1 - i))
            d -= dx
            if d < 0:
                d += dy
                x -= 1
    else:
        # Forward: start at (x0, y0), walk up, x moves right
        x = x0
        for j in range(dy + 1):
            pixels.add((x, y0 - j))
            d -= dx
            if d < 0:
                d += dy
                x += 1
    return pixels


def reference_vertical(x0, y0, x1, y1):
    """Vertical path reference (dx=0)."""
    x = x0  # x0 == x1
    y_lo = min(y0, y1)
    y_hi = max(y0, y1)
    return {(x, y) for y in range(y_lo, y_hi + 1)}


def reference_line(x0, y0, x1, y1):
    """Dispatch to correct reference based on line slope, matching assembly."""
    # Assembly normalizes x1 >= x0
    if x1 < x0:
        x0, x1 = x1, x0
        y0, y1 = y1, y0
    dx = x1 - x0
    dy = abs(y0 - y1)
    if dx == 0:
        return reference_vertical(x0, y0, x1, y1)
    elif dx >= dy:
        return reference_shallow(x0, y0, x1, y1)
    else:
        return reference_steep(x0, y0, x1, y1)


# ── Screen memory extraction ─────────────────────────────────────────

def pixel_addr_and_mask(x, y):
    """Return (address, bitmask) for pixel (x,y) in the stripe layout."""
    stripe = y >> 3
    row    = y & 7
    col    = x & 0xF8
    addr   = (SCREEN_BASE_HI + stripe) * 256 + col + row
    mask   = 0x80 >> (x & 7)
    return addr, mask

def extract_pixels(mem, x_min, x_max, y_min, y_max):
    """Return set of (x,y) for all set pixels in the given bounding box."""
    pixels = set()
    for y in range(y_min, y_max + 1):
        for x in range(x_min, x_max + 1):
            addr, mask = pixel_addr_and_mask(x, y)
            if mem[addr] & mask:
                pixels.add((x, y))
    return pixels

def extract_all_pixels(mem):
    """Return set of (x,y) for ALL set pixels in screen memory."""
    pixels = set()
    for stripe in range(32):          # 256/8 = 32 stripes
        page = SCREEN_BASE_HI + stripe
        base = page * 256
        for col in range(0, 256, 8):  # 32 byte columns
            for row in range(8):
                byte_val = mem[base + col + row]
                if byte_val:
                    for bit in range(8):
                        if byte_val & (0x80 >> bit):
                            x = col + bit
                            y = stripe * 8 + row
                            pixels.add((x, y))
    return pixels


# ── Test harness assembly generation ─────────────────────────────────

HARNESS_TEMPLATE = """\
; Test harness — generated by validate_bresenham.py
.feature leading_dot_in_identifiers

.segment "CODE"

main:
    CLD                         ; ensure binary mode
    ; ── set screen page ──
    LDA #${screen_hi:02X}
    STA $84                     ; screen_page

    ; ── set coordinates ──
    LDA #{x0}
    STA $80                     ; x0
    LDA #{y0}
    STA $81                     ; y0
    LDA #{x1}
    STA $82                     ; x1
    LDA #{y1}
    STA $83                     ; y1

    ; ── push return address so draw_line's RTS lands at halt ──
    LDA #>(halt-1)
    PHA
    LDA #<(halt-1)
    PHA

    ; ── setup and draw ──
    JMP draw_line

halt:
    STP                         ; $DB — stop the 65C02

; ── include the code under test ──
.include "raster.s"
"""

def generate_harness(x0, y0, x1, y1):
    return HARNESS_TEMPLATE.format(
        screen_hi=SCREEN_BASE_HI,
        x0=x0, y0=y0, x1=x1, y1=y1,
    )


# ── Build & run ──────────────────────────────────────────────────────

def assemble_and_run(harness_src, tmpdir):
    """Assemble, link, run, and return the 64 KB memory image."""
    src_path = os.path.join(tmpdir, "harness.s")
    obj_path = os.path.join(tmpdir, "harness.o")
    bin_path = os.path.join(tmpdir, "harness.bin")
    mem_path = os.path.join(tmpdir, "mem.bin")

    with open(src_path, "w") as f:
        f.write(harness_src)

    # Assemble (use -I so .include "raster.s" finds the source)
    raster_dir = os.path.dirname(RASTER_S)
    r = subprocess.run(
        ["ca65", "--cpu", "65C02", "-I", raster_dir, "-o", obj_path, src_path],
        capture_output=True, text=True,
    )
    if r.returncode != 0:
        raise RuntimeError(f"ca65 failed:\n{r.stderr}")

    # Link
    r = subprocess.run(
        ["ld65", "-C", LINKER_CFG, "-o", bin_path, obj_path],
        capture_output=True, text=True,
    )
    if r.returncode != 0:
        raise RuntimeError(f"ld65 failed:\n{r.stderr}")

    # Run
    r = subprocess.run(
        [RUNNER, bin_path, mem_path],
        capture_output=True, text=True,
    )
    if r.returncode not in (0, 2):
        raise RuntimeError(f"runner failed (rc={r.returncode}):\n{r.stderr}")
    if r.returncode == 2:
        raise RuntimeError(f"runner hit cycle limit (CPU did not halt):\n{r.stderr}")

    with open(mem_path, "rb") as f:
        mem = f.read()
    if len(mem) != 65536:
        raise RuntimeError(f"memory dump is {len(mem)} bytes, expected 65536")

    return mem


# ── Visualisation ────────────────────────────────────────────────────

def visual_diff(emu_pixels, ref_pixels, x0, y0, x1, y1):
    """
    Return a string grid:
      '#' = both      'E' = emulator only      'R' = reference only
      '.' = neither
    """
    x_lo = min(x0, x1)
    x_hi = max(x0, x1)
    y_lo = min(y0, y1)
    y_hi = max(y0, y1)
    lines = []
    # Header with x coords (ones digit)
    hdr = "     "
    for x in range(x_lo, x_hi + 1):
        hdr += str(x % 10)
    lines.append(hdr)
    for y in range(y_lo, y_hi + 1):
        row = f"y={y:3d} "
        for x in range(x_lo, x_hi + 1):
            e = (x, y) in emu_pixels
            r = (x, y) in ref_pixels
            if e and r:
                row += "#"
            elif e:
                row += "E"
            elif r:
                row += "R"
            else:
                row += "."
        lines.append(row)
    return "\n".join(lines)


