def generate_shallow_soak(count=5000, seed=42):
    """Generate random shallow test cases (dx >= dy)."""
    rng = random.Random(seed)
    cases = []
    for i in range(count):
        dx = rng.randint(1, 255)
        dy = rng.randint(0, dx)
        x0 = rng.randint(0, 255 - dx)
        x1 = x0 + dx
        if rng.random() < 0.5:
            y1 = rng.randint(0, 255 - dy)
            y0 = y1 + dy
        else:
            y0 = rng.randint(0, 255 - dy)
            y1 = y0 + dy
        name = f"shallow soak {i}: ({x0},{y0})->({x1},{y1})"
        cases.append((name, x0, y0, x1, y1))
    return cases


def generate_steep_soak(count=2000, seed=99):
    """Generate random steep test cases (dy > dx)."""
    rng = random.Random(seed)
    cases = []
    for i in range(count):
        dy = rng.randint(2, 255)
        dx = rng.randint(1, dy - 1)
        x0 = rng.randint(0, 255 - dx)
        x1 = x0 + dx
        if rng.random() < 0.5:
            y1 = rng.randint(0, 255 - dy)
            y0 = y1 + dy
        else:
            y0 = rng.randint(0, 255 - dy)
            y1 = y0 + dy
        name = f"steep soak {i}: ({x0},{y0})->({x1},{y1})"
        cases.append((name, x0, y0, x1, y1))
    return cases


def generate_vertical_soak(count=500, seed=77):
    """Generate random vertical test cases (dx=0)."""
    rng = random.Random(seed)
    cases = []
    for i in range(count):
        x = rng.randint(0, 255)
        dy = rng.randint(0, 255)
        if rng.random() < 0.5:
            y1 = rng.randint(0, 255 - dy)
            y0 = y1 + dy
        else:
            y0 = rng.randint(0, 255 - dy)
            y1 = y0 + dy
        name = f"vert soak {i}: ({x},{y0})->({x},{y1})"
        cases.append((name, x, y0, x, y1))
    return cases


# ── Main ─────────────────────────────────────────────────────────────

def main():
    # Ensure runner is built
    if not os.path.isfile(RUNNER):
        print(f"Building {RUNNER}...")
        r = subprocess.run(
            ["cc", "-O2", "-o", RUNNER,
             os.path.join(TOOLS_DIR, "w65c02s_runner.c"), "-I", TOOLS_DIR],
            capture_output=True, text=True,
        )
        if r.returncode != 0:
            print(f"Failed to build runner:\n{r.stderr}", file=sys.stderr)
            sys.exit(1)

    passes = 0
    fails  = 0

    all_cases = (TEST_CASES
                 + generate_shallow_soak()
                 + generate_steep_soak()
                 + generate_vertical_soak())

    with tempfile.TemporaryDirectory(prefix="raster_test_") as tmpdir:
        for name, x0, y0, x1, y1 in all_cases:
            dx = x1 - x0
            dy = abs(y0 - y1)
            tag = f"({x0},{y0})->({x1},{y1}) dx={dx} dy={dy}"
            print(f"  {name:40s} {tag} ... ", end="", flush=True)

            ref = reference_line(x0, y0, x1, y1)

            src = generate_harness(x0, y0, x1, y1)
            try:
                mem = assemble_and_run(src, tmpdir)
            except RuntimeError as e:
                print(f"BUILD/RUN ERROR\n{e}")
                fails += 1
                continue

            emu = extract_all_pixels(mem)

            if emu == ref:
                print("PASS")
                passes += 1
            else:
                print("FAIL")
                fails += 1
                print(visual_diff(emu, ref, x0, y0, x1, y1))
                only_emu = emu - ref
                only_ref = ref - emu
                if only_emu:
                    print(f"    Emulator-only pixels: {sorted(only_emu)}")
                if only_ref:
                    print(f"    Reference-only pixels: {sorted(only_ref)}")
                print()

    print(f"\n{'='*60}")
    print(f"  {passes} passed, {fails} failed, {passes+fails} total")
    if fails:
        sys.exit(1)
    else:
        print("  All tests passed!")


if __name__ == "__main__":
    main()
